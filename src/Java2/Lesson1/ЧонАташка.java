package Java2.Lesson1;

    public class ЧонАташка {
        private int age;
        private String name;


        public ЧонАташка(int age, String name) {
            this.age = age;
            this.name = name;
        }

        public int getAge() {
            return age;
        }

        public String getName() {
            return name;
        }


    }